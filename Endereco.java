package fatec.pg.Model;

public class Endereco {
			
	private String endereco;
	private int numero;
	
	public Endereco(String endereco, int numero) {
		this.endereco = endereco;
		this.numero = numero;
	}
	
	
	 public String getEndereco() {
	        return endereco + ", " + numero;
	    }

	
		
		


	

	

	

}
